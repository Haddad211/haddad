const mongoose = require('mongoose');

const innovatorMessageSchema = new mongoose.Schema({
  senderId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  receiverId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  content: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

const InnovatorMessage = mongoose.model('InnovatorMessage', innovatorMessageSchema);

module.exports = InnovatorMessage;
